package com.demo.daytwo;

public class Pattern {
	public static void main(String[] args) {
		
		int n = 5;
		
		for (int i = n; i>=1; i--) {
			for (int j =n; j>=i; j--) {
				System.out.println(j);
			}
				
				for(int k=n; k>=i; k--) {
					System.out.println(i);
				}
				System.out.println();
			} 
	}
}
		

